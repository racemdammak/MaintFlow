    private void validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerActionPerformed
        String description = idescription.getText();
        String numImm = (String) inumImm.getSelectedItem();
        Date date = idate.getDate();
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        String heure = iheure.getText();
        String technicien = (String) itechnicien.getSelectedItem();
        String statut = (String) istatut.getSelectedItem();
        
        if (description.isEmpty()|| heure.isEmpty() || technicien.isEmpty()){
            JOptionPane.showMessageDialog(this, 
                "Veuillez remplir tous les champs",
                "Erreur de validation", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/users", "root", "");
             PreparedStatement pstmt = conn.prepareStatement(
                 "INSERT INTO interventions  (description, date, heure, statut, technicien, NumImm ,id_user) " +
                 "VALUES ( ?, ?, ?, ?, ?, ?, ?)")) {

            pstmt.setString(1, description);
            pstmt.setDate(2, sqlDate);
            pstmt.setString(3, heure);
            pstmt.setString(4, statut);
            pstmt.setString(5, technicien);
            pstmt.setString(6, numImm);
            pstmt.setInt(7, userId);

            int rowsAffected = pstmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "DI ajouté avec succès!");

                this.dispose();
                Intervention fr = new Intervention(userId);
                fr.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "L'ajout a échoué", "Erreur", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Erreur de base de données: " + e.getMessage(), 
                "Erreur", 
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_validerActionPerformed

