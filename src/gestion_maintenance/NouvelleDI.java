    private void loadVehicules() {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/users", "root", "");
             PreparedStatement pst = conn.prepareStatement("SELECT numImm FROM vehicules WHERE id_user = ?")) {

            pst.setInt(1, userId);
            ResultSet rs = pst.executeQuery();

            inumImm.removeAllItems();
            inumImm.addItem("-- Sélectionnez un véhicule --");

            while (rs.next()) {
                // Conversion explicite de l'Integer en String
                String numImmStr = rs.getString("NumImm");
                inumImm.addItem(String.valueOf(numImmStr));
            }

            if (inumImm.getItemCount() == 1) { // Seul l'item par défaut
                inumImm.addItem("Aucun véhicule disponible");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, 
                "Erreur de chargement des véhicules: " + e.getMessage(),
                "Erreur BD",
                JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void loadTechniciens() {
    try {
        // Connexion
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/users", "root", "");

        String sql = "SELECT * FROM techniciens WHERE id_user = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, userId);

        ResultSet rs = pst.executeQuery();

        itechnicien.removeAllItems();
        itechnicien.addItem("-- Sélectionnez un Technicien --");

        while (rs.next()) {
            String nomTech = rs.getString("technicien");
            itechnicien.addItem(nomTech);
        }

        rs.close();
        pst.close();
        conn.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Erreur lors du chargement des techniciens : " + e.getMessage());
    }
}

    
