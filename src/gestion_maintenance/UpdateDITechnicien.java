    private void terminerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_terminerActionPerformed
        try (Connection conn = DriverManager.getConnection(
            "jdbc:mysql://localhost:3306/users", "root", "")) {

            java.util.Date utilDate = idate.getDate();
            java.sql.Date sqlDate1 = new java.sql.Date(utilDate.getTime());

            // 3. Mettre à jour l'intervention
            try (PreparedStatement pstmt = conn.prepareStatement(
                "UPDATE interventions SET statut = ? WHERE NumImm = ? AND id_user = ?")) {

                pstmt.setString(1, "Terminée");
                pstmt.setString(2, numImm);
                pstmt.setInt(3, userId);
                pstmt.executeUpdate();
            }

            // 4. Mettre à jour le véhicule
            try (PreparedStatement pstmt2 = conn.prepareStatement(
                "UPDATE vehicules SET statut = ? WHERE NumImm = ? and id_user = ?")) {

                pstmt2.setInt(1, 0);
                pstmt2.setString(1, "Actif");
                pstmt2.setString(2, numImm);
                pstmt2.setInt(3, userId);
                pstmt2.executeUpdate();
            }

            // 5. Insérer dans archive
            try (PreparedStatement pstmt3 = conn.prepareStatement(
                "INSERT INTO archive (NumImm, date_maintenance, operation, id_user, id_admin) VALUES (?, ?, ?, ?, ?)")) {

                pstmt3.setString(1, numImm);
                pstmt3.setDate(2, sqlDate1);
                pstmt3.setString(3, operation);
                pstmt3.setInt(4, userId);
                pstmt3.setInt(4, adminId);
                pstmt3.executeUpdate();
            }

            // 6. Supprimer l'intervention
            try (PreparedStatement pstmt4 = conn.prepareStatement(
                "DELETE FROM interventions WHERE numImm = ? and id_user = ?")) {

                pstmt4.setString(1, numImm);
                pstmt4.setInt(2, userId);
                pstmt4.executeUpdate();
            }

            JOptionPane.showMessageDialog(this,
                "Opération terminée avec succès!",
                "Succès", JOptionPane.INFORMATION_MESSAGE);

            new InterventionTechnicien(userId).setVisible(true);
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Erreur de base de données: " + ex.getMessage(),
                "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_terminerActionPerformed

